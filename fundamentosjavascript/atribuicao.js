//existe o operador de atribuição e algumas variações de atribuição usando algumas operações aritméticas
const a = 7 //o simbolo de iguale stá fazendo uma atribuição, está colocando esse valor 7 dentro da variavel a 
let b = 3 //

b += a //junto da atribuição usando operações aritimétricas 
        //+= atribuição aditiva, acrecentando o valor de a para a variavel b 
        //b = b + a
console.log(b)

b -= 4  //subtraindo o valor 4 de b 
        //b = b -4
console.log(b)

b *+ 2 //b = b * 2

console.log(b)

b /= 2 //b = /2
console.log(b)

b %= 2 //b = b %2 se for par o resultado é zero, se for impar o resultado é um
